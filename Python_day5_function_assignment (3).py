# Using max() and min() functions display the maximum and minimum of 5 random numbers.
numbers = [55, 27, 15, 32, 5, 3]

# Display the numbers
print("The numbers are:", numbers)

# Find the maximum number using the max() function
maximum_number = max(numbers)

# Find the minimum number using the min() function
minimum_number = min(numbers)

# Display the maximum and minimum numbers
print("The maximum number is:", maximum_number)
print("The minimum number is:", minimum_number)

# ans
# The numbers are: [55, 27, 15, 32, 5, 3]
# The maximum number is: 55
# The minimum number is: 3