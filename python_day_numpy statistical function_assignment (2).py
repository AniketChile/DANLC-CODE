# 2.Compute the standard deviation of the NumPy array

#  Input: arr = [20, 2, 7, 1, 34]

import numpy as np

# Define the NumPy array
arr = np.array([20, 2, 7, 1, 34])

# Compute the standard deviation of the array
# The standard deviation measures the amount of variation or dispersion in the dataset.
std_dev = np.std(arr)

# Print the standard deviation value
print(std_dev)  

# ans
# 12.576167937809991 
